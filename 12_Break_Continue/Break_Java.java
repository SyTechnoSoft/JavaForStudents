public class Break_Java {
    
    public static void main(String[] args){
        for(int i=0; i<10; i++){
            if(i==5){
                break;
            }
            System.out.println(i);
        }
    }
}
/*The break statement can also be used to jump out of a loop.

This example stops the loop when i is equal to 5: */
