public class ElseIf {
    public static void main (String[] args){
        int a=10;
        int b=1;
        if (a==b){
            System.out.println("if statement is true");
        
        }else if (a<b){
            System.out.println("else if statement is true");
            
        }else{
            System.out.println("else statement is true");
        }
    }
    
}
