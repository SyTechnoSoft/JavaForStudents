public class Rhombus {
    public static void main (String[] args ){
    
    
        for (int i=1; i<=4;i++){
            for (int j=0;j<4;j++){
                System.out.print("*");

            }
            
            System.out.println("");
            for (int k=0;k<i;k++){
                System.out.print(" ");
            }
        }
    

    }
    
}
