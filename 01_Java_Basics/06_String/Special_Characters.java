public class Special_Characters {
    public static void main(String[] args) {

        String txt = "1 We are the so-called \"Vikings\" from the north.";
        String txt1 = "2 The character \\ is called backslash.";
        String txt2 = "3 It\'s alright.";
        String txt3 = "4 The character \n is called newline.";
        String txt4 = "5 The character \t is called tab.";
        String txt5 = "6 The character \r is called carriage return.";
        String txt6 = "7 The character \f is called form feed.";
        String txt7 = "8 The character B\b is called backspace.";


        System.out.println(txt);
        System.out.println(txt1);
        System.out.println(txt2);
        System.out.println(txt3);
        System.out.println(txt4);
        System.out.println(txt5);
        System.out.println(txt6);
        System.out.println(txt7);

    }
    
}
