
public class WhileLoop {
    public static void main(String[] argh)
    {
        int i=1;
        // 1 to 10 table using while loop
        while(i<=10)
        {
            System.out.print((i*2)+" ");
        
           i++;
        }
        int j=2;
        while (j<=10) {
            System.out.println(j+" ");
            j+=2;
            
        }
        

        
    }
}
