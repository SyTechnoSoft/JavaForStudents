import java.util.HashSet; // Import the HashSet class

public class AddItemsHashSet {

    public static void main(String[] args) {
        HashSet<String> student =new HashSet<String>();
        student.add("Saurabh");
        student.add("Gaurav");
        student.add("Abhishek");

        System.out.println(student);
    }
    
}
